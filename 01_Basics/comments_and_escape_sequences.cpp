#include <iostream>
using namespace std;

int main()
{
    // This is a single-line comment
    /*
       This is a multi-line comment
       explaining the use of escape sequences
    */

    cout << "Hello\tWorld!\n"; // \t adds a tab, \n adds a new line
    cout << "This is \"C++\" programming!" << endl;

    return 0;
}
